
/**
 * @mainpage SimNet - Network layer for simulations
 *
 * @section overview Overview
 *
 * SimNet is a cross-platform library for network communications between
 * distributed components in a simulation.  The library is written in C++,
 * and relies on GNU Common C++ for low-level network access.  SimNet
 * currently supports reliable and unreliable message transport using
 * UDP.  The primary communications model combines a client-server
 * interface with peer-to-peer communications between clients.
 *
 */
